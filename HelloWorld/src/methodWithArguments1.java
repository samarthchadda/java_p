
public class methodWithArguments1 {

	
	public static int integerSum(int i, int j)
	{
		return i+j;
		
	}
	
	public static int multiplyInteger(int x,int y)
	{
		return x*y;
				
	}
	
	
	public static void main(String[] args) {
		
		System.out.println("10 + 20 = "+integerSum(10,20));
		System.out.println("20 X 40 = "+multiplyInteger(20,40));

	}

}
